                Тепер Ви бачите перед собою оголошення про закупівлю. В оголошенні зазвичай замовники прописують основну інформацію про закупівлю, а саме, хто, що, скільки хоче закупити, куди має постачатись товар, на яких умовах тощо. Вже більш детально все описано в тендерній документації, яка прикріплюється замовником до оголошення. Тож, на що звертати увагу, коли Ви бачите оголошення про закупівлю.
            </p>
        </div>
        <div class="row">
            <div class="col-md-4">
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Очікувана вартість закупівлі. </span>Це саме та сума, яку планує витратити замовник на закупівлю товару. Зверніть увагу, що при подачі своєї пропозиції Ви не можете поставити ціну своєї пропозиції більше, ніж очікувана вартість. Зате якщо Ви подасте пропозицію з найнижчою ціною - це дасть Вам певні переваги під час аукціону, які саме, Ви можете прочитати в нашій статті <a href="http://infobox.prozorro.org/knowledge-base/view/684?q=/?utm_source=newsletter&utm_medium=email" target="_blank" class="link">“Аукціон”</a> .
                </p>

                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Звернення за роз’ясненнями.</span> До дати, зазначеної в цьому полі, Ви можете через особистий кабінет на майданчику поставити запитання замовнику, щоб уточнити якусь інформацію про закупівлю чи поставити вимогу внести зміни в тендерну документацію. Детальніше про це далі.
                </p>

                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Оскарження умов закупівлі.</span> Там завжди прописані дата та час, до яких Ви маєте можливість подати скаргу в АМКУ на умови закупівлі (якщо Ви бачите якісь дискримінаційні вимоги від замовника тощо).
                </p>

                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Початок аукціону.</span> В цьому полі зазначені дата та час, коли буде розпочато аукціон.
                </p>
            </div>
            <div class="col-md-4">
                <p class="p-cont">
                    До початку аукціону Вам в особистий кабінет на майданчику прийде повідомлення про аукціон та посилання для входу в аукціон, за яким Ви маєте перейти.
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Кінцевий строк подання тендерних пропозицій.</span> Це той строк до якого Ви маєте подати свою тендерну пропозицію. Про те, які саме документи зазвичай мають бути подані в тендерній пропозиції Ви можете прочитати далі.
                </p>
