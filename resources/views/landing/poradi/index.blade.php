                    Коли Ви подаєте пропозицію, то мінімальний крок пониження ціни для Вас не важливий.
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Місце поставки.</span> Майте на увазі, що не завжди Місцезнаходження замовника є місцем поставки товару. Місце, куди саме буде постачатись товар має бути зазначено в Інформації про закупівлі на
                    <a href="/landing/poradi/img/3.jpg" class="link" data-lity>малюнку <i class="fa fa-hand-pointer-o" aria-hidden="true" ></i>
                        <span class="img-h img-h4">
                            <img src="/landing/poradi/img/3.jpg" alt="">
