                </p>
            </div>
        </div>
        <div class="row">
            <div class="col-md-4">
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Поставити запитання замовнику.</span>  Для уточ-нення якоїсь інформації стосовно закупівлі Ви можете поставити запитання замовнику через систему на яке він має відповісти так само через систему.
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Поставити вимогу замовнику.</span> Ви можете поставити вимогу замовнику, щоб він вніс зміни в умови закупівлі. Він має відповісти Вам через систему на цю вимогу та якщо вона аргументована - внести зміни в тендерну документацію
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Подати скаргу (тільки для понадпорогових закупівель).</span> Ви можете подати скаргу через систему, якщо побачили дискримінаційні умови в тендерній документації, які замовник не бажає змінювати чи вважаєте якісь дії замовника неправомірними. Після подачі Вами скарги, вона автоматично направляється в АМКУ.
                </p>
                <p class="p-cont">
                    <span class="accent">Важливо!</span>
                    <br>
                    <span class="abz"></span>
                    <span class="accent">-</span> Звертатись з вимогою чи запитанням чи скаргою може тільки постачальник, що зареєстрований на електронному майданчику.
                </p>
            </div>
            <div class="col-md-4">
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">-</span> Система “не розуміє” суті запитань/від-повідей, вона перевіряє лише заповнення відповідних полів у зазначені строки.
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
                    Зважаючи на те, що процес проведення допорогової закупівлі та понадпорогової закупівлі є різним, ми окремо розберемо можливості Вашої, так би мовити, комунікації між замовником та постачальником для цих двох видів закупівель.
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
