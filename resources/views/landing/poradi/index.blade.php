                    <span class="abz"></span>
                    <span class="accent">Вид тендерного забезпечення.</span> Якщо замовник не передбачив надання постачальником забезпечення тендерної пропозиції, тоді в цьому полі Ви побачите слово “відсутній”. Якщо замовник передбачив забезпечення (тендерної пропозиції), тоді у Вас рядок “Вид тендерного забезпечення” буде відображатись так, як на <a href="/landing/poradi/img/2.jpg" class="link" data-lity target="_blank">малюнку <i class="fa fa-hand-pointer-o" aria-hidden="true"></i> <span class="img-h img-h2">
