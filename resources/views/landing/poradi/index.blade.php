                    <span class="abz"></span>
                    <span class="accent">-</span> довідка про відсутність не знятої чи непогашеної у встановленому порядку судимості за злочини, вчинені з корисливих мотивів. Зазвичай ця довідка про керівника підприємства та/або особу, що підписувала тендерну пропозицію (якщо підписував не керівник, а інша уповноважена на це особа).
                    Ця довідка видається уповноваженим органом МВС, зазвичай час видачі довідки складає від 5 робочих днів. Детальніше про те, де отримати цю довідки, як оформити запит на отримання тощо Ви можете прочитати за <a href="http://mvs.gov.ua/ua/pages/Dovidka_pro_nesudimist___katalog_poslug.htm/?utm_source=newsletter&utm_medium=email" target="_blank" class="link">посиланням</a>
