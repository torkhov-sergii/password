                </p>
            </div>
            <div class="col-md-4">
                <p class="p-cont">
                    тендерній документації.
                </p>
                <p class="p-cont">
                    <span class="abz"></span>
                    <span class="accent">Розмір мінімального кроку пониження ціни.</span> Мінімальний крок пониженні ціни це мінімальна сума на яку Ви можете понизити свою ставку в кожному раунді (по відношенню до своєї попередньої ставки).
